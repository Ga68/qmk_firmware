uint32_t oled_timeout_user(void) {
    led_t led_usb_state = host_keyboard_led_state();

    if (led_usb_state.caps_lock) {
        return (uint32_t)1000 * 60 * 60; // 60 minutes
    }
    return (uint32_t)1000 * 30; // 30 seconds
}


